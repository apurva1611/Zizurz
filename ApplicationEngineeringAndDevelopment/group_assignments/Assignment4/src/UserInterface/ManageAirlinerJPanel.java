    private void btnViewAirlineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewAirlineActionPerformed
        int selectedRow = tblAirlines.getSelectedRow();
        if(selectedRow>=0){
              Airliner airline= (Airliner)tblAirlines.getValueAt(selectedRow, 0);
              ViewAirlineJPanel viewJPanel = new ViewAirlineJPanel(cardSequenceJPanel,airline,airlineDirectory,flightSchedules);
              cardSequenceJPanel.add("ViewJPanel",viewJPanel);
              CardLayout layout = (CardLayout) cardSequenceJPanel.getLayout();
              layout.next(cardSequenceJPanel);
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a row from table first to view Details!","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnViewAirlineActionPerformed

    private void btnDeleteAirlineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAirlineActionPerformed
        int selectedRow = tblAirlines.getSelectedRow();
        if(selectedRow>=0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                Airliner airline= (Airliner)tblAirlines.getValueAt(selectedRow, 0);
                this.airlineDirectory.deleteAirline(airline);
                populateTable(this.airlineDirectory.getAirlinerList());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_btnDeleteAirlineActionPerformed

