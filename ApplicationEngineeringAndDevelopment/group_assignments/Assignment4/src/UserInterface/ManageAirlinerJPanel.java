    public void populateTable(ArrayList<Airliner> airlines){
        DefaultTableModel dtm = (DefaultTableModel) tblAirlines.getModel();
        dtm.setRowCount(0);
        for(Airliner al : airlines){
            Object row[]= new Object[3];
            row[0] = al;
            row[1] = al.getFleet().getAirplanes().size();
            row[2]=al.getFlights().getFlights().size();
            dtm.addRow(row);
        }
    }

