    private void populateTable(ArrayList<Airplane> airplanes) {
        DefaultTableModel dtm = (DefaultTableModel) tblAirplanes.getModel();
        dtm.setRowCount(0);
        for(Airplane ap : airplanes){
            Object row[]= new Object[3];
            row[0] = ap;
            row[1] = ap.getFuelCapacity();
            row[2]=  ap.getSerialNumber();
            dtm.addRow(row);
        }
    }

