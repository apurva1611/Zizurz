    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(txtAirlineName.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Please fill airline name");
           return;
       }
       txtAirlineName.setEnabled(false);
       btnSave.setEnabled(false);
       btnUpdate.setEnabled(true);
       this.airline.setAirLinerName(txtAirlineName.getText());
       this.flightSchedules.getFlightSchedules().put(txtAirlineName.getText(), this.flightSchedules.getFlightSchedules().get(airlineName));
       this.flightSchedules.getFlightSchedules().remove(airlineName);
       JOptionPane.showMessageDialog(null, "Airline updated successfully");
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteAirplaneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAirplaneActionPerformed
        int selectedRow = tblAirplanes.getSelectedRow();
        if(selectedRow>=0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                Airplane airplane= (Airplane)tblAirplanes.getValueAt(selectedRow, 0);
                this.airlineDirectory.deleteAirplane(airplane,airline);
                populateTable(airline.getFleet().getAirplanes());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_btnDeleteAirplaneActionPerformed

    private void btnViewAirplaneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewAirplaneActionPerformed
        int selectedRow = tblAirplanes.getSelectedRow();
        if(selectedRow>=0){
              Airplane airplane= (Airplane)tblAirplanes.getValueAt(selectedRow, 0);
              ViewAirplaneJPanel viewJPanel = new ViewAirplaneJPanel(cardSequenceJPanel,airline,airlineDirectory,flightSchedules,airplane);
              cardSequenceJPanel.add("ViewAirplaneJPanel",viewJPanel);
              CardLayout layout = (CardLayout) cardSequenceJPanel.getLayout();
              layout.next(cardSequenceJPanel);
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a row from table first to view Details!","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnViewAirplaneActionPerformed

