    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblFlights.getSelectedRow();
        if (selectedRow >= 0)
        {
            Flight flight = (Flight)tblFlights.getValueAt(selectedRow, 0);
            ViewFlightJPanel panel = new ViewFlightJPanel(cardSequenceJPanel, flight, al);
            CardLayout layout = (CardLayout) cardSequenceJPanel.getLayout();
            cardSequenceJPanel.add("ViewFlightAirlines", panel);
            layout.next(cardSequenceJPanel);
        }

        else{
            JOptionPane.showMessageDialog(null, "Please select a Flight first.");
            return;
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnDeleteFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteFlightActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblFlights.getSelectedRow();
        if(selectedRow>=0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this Flight?", "Warning", selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                Flight f = (Flight)tblFlights.getValueAt(selectedRow, 0);
                al.getFlights().getFlights().remove(f);
                populateTable();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_btnDeleteFlightActionPerformed

