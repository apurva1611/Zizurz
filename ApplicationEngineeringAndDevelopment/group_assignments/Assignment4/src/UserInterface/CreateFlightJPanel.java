    private boolean DatePatternCorrect() {
       Pattern p = Pattern.compile("^[*\\d]+/[*\\d]+/[*\\d]+$");
       Matcher m = p.matcher(textfieldFlightDate.getText());
       boolean b = m.matches();
       return b;
   }
    
    private boolean DepartTimePatternCorrect() {
       Pattern p = Pattern.compile("^[*\\d]+:[*\\d]+$");
       Matcher m = p.matcher(textfieldDepartTime.getText());
       boolean b = m.matches();
       return b;
   }
    
    private boolean ArrivalTimePatternCorrect() {
       Pattern p = Pattern.compile("^[*\\d]+:[*\\d]+$");
       Matcher m = p.matcher(textfieldArrivalTime.getText());
       boolean b = m.matches();
       return b;
   }

