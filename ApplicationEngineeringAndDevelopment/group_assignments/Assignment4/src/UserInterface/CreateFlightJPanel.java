    private void btnAddFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFlightActionPerformed
        String modelno = (String) comboBoxFlights.getSelectedItem();
        if(textfieldFlightDate.getText().equals("") ||
           textfieldArrivalCity.getText().equals("") || 
           textfieldDepartureCity.getText().equals("") ||
           textfieldDepartTime.getText().equals("") || 
           textfieldArrivalTime.getText().equals("")|| 
           textfieldPrice.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please enter all the fields!");
            return;
        }
        if(modelno.equalsIgnoreCase("--Select--")) {
            JOptionPane.showMessageDialog(null, "Please select an airplane!");
            return;
        }
        
        if(!DatePatternCorrect() || textfieldFlightDate.getText().length()!=10) {
            JOptionPane.showMessageDialog(null, "Please enter Date in MM-DD-YYYY format");
            return;
        }
        
        if(!DepartTimePatternCorrect() || textfieldDepartTime.getText().length()!=5) {
            JOptionPane.showMessageDialog(null, "Please enter Departure Time in HH:MM format");
            return;
        }
        
        if(!ArrivalTimePatternCorrect() || textfieldArrivalTime.getText().length()!=5) {
            JOptionPane.showMessageDialog(null, "Please enter Arrival Time in HH:MM format");
            return;
        }
        
        
            Flight f = new Flight();
            f.setFlyDate(textfieldFlightDate.getText());
            f.setArrivalCity(textfieldArrivalCity.getText());
            f.setDepartureCity(textfieldDepartureCity.getText());
            f.setDeparturetime(textfieldDepartTime.getText());
            f.setArrivaltime(textfieldArrivalTime.getText());
            f.setAirlineName(al.getAirLinerName());
            Airplane airplane = al.getFleet().findAirplaneByModelNumber(modelno);
            f.setPrice(Double.parseDouble(textfieldPrice.getText()));
            f.setAirplane(airplane);
            al.getFlights().getFlights().add(f);

            JOptionPane.showMessageDialog(null, "Flight created successfully for " + modelno);
        
    
    }//GEN-LAST:event_btnAddFlightActionPerformed

