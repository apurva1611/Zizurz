    private void cancelBookingBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelBookingBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblBookingHistory.getSelectedRow();
        if (selectedRow >= 0) {
            int input = JOptionPane.showConfirmDialog(null, "Are you sure you want to cancel this booking?");
            if (input == 0) {
                Ticket t = (Ticket) tblBookingHistory.getValueAt(selectedRow, 0);
                bookingHistory.getBookingHistory().remove(t);

                Flight f = (Flight) tblBookingHistory.getValueAt(selectedRow, 2);
                String seatNumber = (String)tblBookingHistory.getValueAt(selectedRow, 5);
                f.getSeat().getSeatNumber().replace(seatNumber,0,1);
                populateHistory();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a ticket to proceed.");
            return;
        }

    }//GEN-LAST:event_cancelBookingBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        String search = searchField.getText();
        DefaultTableModel dtm = (DefaultTableModel) tblBookingHistory.getModel();
        dtm.setRowCount(0);
        for (Ticket ticket : bookingHistory.getBookingHistory()) {
            if (ticket.getCustomer().getName().equalsIgnoreCase(search)) {
                Object[] row = new Object[dtm.getColumnCount()];
                row[0] = ticket.getCustomer().getName();
                row[1] = ticket.getCustomer().getContactNumber();
                row[2] = ticket.getFlight();
                row[3] = ticket.getFlight().getDepartureCity();
                row[4] = ticket.getFlight().getArrivalCity();
                row[5] = ticket.getSeatNumber();
                row[6] = ticket.getFlight().getPrice();
                row[7] = ticket.getFlight().getAirlineName();
                dtm.addRow(row);
            }
        }
        
    }//GEN-LAST:event_searchBtnActionPerformed

