    public void populateHistory() {
        DefaultTableModel dtm = (DefaultTableModel) tblBookingHistory.getModel();
        dtm.setRowCount(0);
        for (Ticket ticket : bookingHistory.getBookingHistory()) {
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = ticket;
            row[1] = ticket.getCustomer().getContactNumber();
            row[2] = ticket.getFlight();
            row[3] = ticket.getFlight().getDepartureCity();
            row[4] = ticket.getFlight().getArrivalCity();
            row[5] = ticket.getSeatNumber();
            row[6] = ticket.getFlight().getPrice();
            row[7] = ticket.getFlight().getAirlineName();
            dtm.addRow(row);
        }
    }

