    private void btnRegisterAirplanesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterAirplanesActionPerformed
        if ((txtFuelCapacity.getText().equals("")) || (txtModelNum.getText().equals("")) || (txtSerialNum.getText().equals(""))) {
            JOptionPane.showMessageDialog(null, "Please enter the required fields");
            return;
        }
        try {
            Double.parseDouble(txtFuelCapacity.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please type in a number for fuel capacity");
            return;
        }
        try {
            Integer.parseInt(txtSerialNum.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please type in a number for Serial Number");
            return;
        }

        Airplane a = new Airplane();
        a.setFuelCapacity(Double.parseDouble(txtFuelCapacity.getText()));
        a.setSerialNumber(Integer.parseInt(txtSerialNum.getText()));
        a.setModelNumber(txtModelNum.getText());
        ArrayList<Airliner> airlines = airlineDirectory.getAirlinerList();
        Airliner airline = airlines.get(airlines.size()-1);
        airline.getFleet().getAirplanes().add(a);
        txtFuelCapacity.setText("");
        txtSerialNum.setText("");
        txtModelNum.setText("");
        JOptionPane.showMessageDialog(null, "Airplane registered successfully!");

    }//GEN-LAST:event_btnRegisterAirplanesActionPerformed

