    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        String name = txtName.getText();
        if (name.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter a name");
            return;
        }
        Airliner al = airlineDirectory.registerAirlines();
        this.airline = al;
        al.setAirLinerName(name);
        al.setOperatingSince();
        flightSchedules.getFlightSchedules().put(al.getAirLinerName(),al.getFlights());
        System.out.println(flightSchedules.getFlightSchedules());
        JOptionPane.showMessageDialog(null, "Airline registered successfully!\nYou can proceed to add airplanes for this flight...");
        
        jLabel2.setEnabled(true);
        jLabel3.setEnabled(true);
        jLabel4.setEnabled(true);
        jLabel5.setEnabled(true);
        txtFuelCapacity.setEnabled(true);
        txtModelNum.setEnabled(true);
        txtSerialNum.setEnabled(true);
        btnRegisterAirplanes.setEnabled(true);
    }//GEN-LAST:event_btnCreateActionPerformed

