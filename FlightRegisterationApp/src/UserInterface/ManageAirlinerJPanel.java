    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblAirlines.getModel();
        dtm.setRowCount(0);
        for(Airliner al : airlineDirectory.getAirlinerList()){
            Object row[]= new Object[2];
            row[0] = al;
            row[1] = al.getFleet().getAirplanes().size();
            dtm.addRow(row);
        }
    }

