    private void btnManageFlightsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManageFlightsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAirlines.getSelectedRow();
        if(selectedRow<0) {
            JOptionPane.showMessageDialog(null, "Please select an Airline first");
            return;
        }

        else
        {
            Airliner al = (Airliner)tblAirlines.getValueAt(selectedRow, 0);
            ManageFlightsJPanel panel = new ManageFlightsJPanel(cardSequenceJPanel,al);
            CardLayout layout = (CardLayout)cardSequenceJPanel.getLayout();
            cardSequenceJPanel.add("ManageFlightsJPanel",panel);
            layout.next(cardSequenceJPanel);
        }

    }//GEN-LAST:event_btnManageFlightsActionPerformed

